package com.cn.entity;

/**
 *
 * @author LFeng
 */
public class Distributor {
    private int distributorID;
    private String distributorUserName;
    private String distributorPassword;
    private String distributorName;
    private String distributorAddress;
    private String distributorPhoneNumber;
    private String distributorType;
    private String distributorCompany;
    private String loginImei;

    public int getDistributorID() {
        return distributorID;
    }

    public void setDistributorID(int distributorID) {
        this.distributorID = distributorID;
    }

    public String getDistributorUserName() {
        return distributorUserName;
    }

    public void setDistributorUserName(String distributorUserName) {
        this.distributorUserName = distributorUserName;
    }

    public String getDistributorPassword() {
        return distributorPassword;
    }

    public void setDistributorPassword(String distributorPassword) {
        this.distributorPassword = distributorPassword;
    }

    public String getDistributorAddress() {
        return distributorAddress;
    }

    public void setDistributorAddress(String distributorAddress) {
        this.distributorAddress = distributorAddress;
    }

    public String getDistributorPhoneNumber() {
        return distributorPhoneNumber;
    }

    public void setDistributorPhoneNumber(String distributorPhoneNumber) {
        this.distributorPhoneNumber = distributorPhoneNumber;
    }

    public String getDistributorType() {
        return distributorType;
    }

    public void setDistributorType(String distributorType) {
        this.distributorType = distributorType;
    }

    public String getDistributorCompany() {
        return distributorCompany;
    }

    public void setDistributorCompany(String distributorCompany) {
        this.distributorCompany = distributorCompany;
    }

    public String getDistributorName() {
        return distributorName;
    }

    public void setDistributorName(String distributorName) {
        this.distributorName = distributorName;
    }

    public String getLoginImei() {
        return loginImei;
    }

    public void setLoginImei(String loginImei) {
        this.loginImei = loginImei;
    }
    
}
